# There are some following data types
# Numeric data types
a1 = 10
print(type(a1)) # integer data type
a2 = 10.7
print(type(a2)) # Float data type
a3 = complex(4,6)
print(type(a3))  # Complex data type

# Sequence data types

b1 = "sachin"
print(type (b1)) # str data type
b2= [1,2,3]
print(type(b2)) # List data type
b3= (1,4,6)
print(type(b3)) # Tuple data type

# Dictionary data type

c1= {"name":"Sachin","age":20,"Branch":"ETC","Year":"3rd"}
print(type(c1))

# set data type

d1 = {1,3,5,6,"sachin"}
print(type(d1))

# Boolean data types
e1 = True
print(type(e1))
e2 = False
print(type(e2))

# Binary data type

F1 = b"sachin"
print(type(F1))